export function getMonthlyData(yearMonth: number): PalmPriceEntry[] {
  const year = Math.floor(yearMonth / 100);
  const month = yearMonth % 100;
  return palmPriceData.filter((e) => e.month === month && e.year === year);
}

export function getLatestPrice(): PalmPriceEntry {
  return palmPriceData[palmPriceData.length - 1];
}

export function getHighestPrice(): PalmPriceEntry {
  return palmPriceData.reduce((max, e) => (e.palmAvg > max.palmAvg ? e : max), palmPriceData[0]);
}

export function getLowestPrice(): PalmPriceEntry {
  return palmPriceData.reduce((min, e) => (e.palmAvg < min.palmAvg ? e : min), palmPriceData[0]);
}
